<?php


class produto {
    protected $nome;
    protected $preco;

    public function __construct($nome,$preco,);
{
    $this->nome = $nome;
    $this->nome = $preco;
}
public function getnome(){
    return $this->nome;
}
public function getPreco(){
    return $this->preco;
}
public function precovenda(){
    return $this->preco;
}

}